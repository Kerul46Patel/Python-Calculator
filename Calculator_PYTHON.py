
# Userinput Value store variable
a = int(input("Enter First Values : "))
b = int(input("Enter Second Values : "))

print("--------------------------------------")

# Userinput Operator store variable
c = str(input("Enter Arithmetic Operator : "))

print("--------------------------------------")

# Perfomance Operation
if(c == "+"):
    print("Ans Is : ",a+b)
elif(c == "-"):
    print("Ans IS : ",a-b)
elif(c == "*"):
    print("Ans IS : ",a*b)
elif(c == "/"):
    print("Ans IS : ",a/b)
else:
    print("Please Valid Operator Enter...")

